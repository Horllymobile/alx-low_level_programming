#include "3-calc.h"
/**
 * op_add - adds two integers
 * @a: integer
 * @b: integer
 * Return: int
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtract two integers
 * @a: integer
 * @b: integer
 * Return: int
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiply two integers
 * @a: integer
 * @b: integer
 * Return: int
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divide two integers
 * @a: integer
 * @b: integer
 * Return: int
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - mod to integers
 * @a: integer
 * @b: integer
 * Return: int
 */

int op_mod(int a, int b)
{
return (a % b);
}
